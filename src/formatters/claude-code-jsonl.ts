import { randomUUID } from 'node:crypto';

import {
  type ClaudeAiContentBlock,
  type ClaudeAiConversation,
  type ClaudeAiMessage,
} from '../importers/claudeai/schema.js';

/**
 * Generate a Claude Code-compatible `.jsonl` from a claude.ai
 * conversation so the chat appears in `/resume` of Claude Code as
 * if it were a local session (Hito 19).
 *
 * The `.jsonl` format is reverse-engineered (no public spec). This
 * generator produces the minimum subset of fields needed for the
 * Claude Code reader to load the file:
 *
 *   - One event per claude.ai message, alternating `user` and
 *     `assistant`. No splitting an assistant turn across multiple
 *     events (real Claude Code does that for multi-block responses
 *     that share a `message.id`; we keep it simple — the reader
 *     accepts both shapes).
 *   - `parentUuid` chains every event to the previous one. The first
 *     event has `parentUuid: null`.
 *   - Common envelope fields (`isSidechain`, `userType`, `entrypoint`,
 *     `cwd`, `gitBranch`, `version`, `sessionId`, `timestamp`)
 *     mirror what real sessions write.
 *   - Synthetic markers tag the import:
 *       - `requestId: "exportal-imported-<sessionId>"`
 *       - `message.id: "msg_imported_<random>"`
 *       - `message.model: "claude-imported-from-claude-ai"`
 *     so the chat is identifiable as imported (not generated by an
 *     Anthropic API call) when inspecting the file by hand.
 *
 * Block conversion (lossy on purpose):
 *   - `text`     → preserved as-is.
 *   - `thinking` → SKIPPED. We can't generate the cryptographic
 *     `signature` Anthropic attaches to thinking blocks, and the
 *     conversation reads fine without them.
 *   - `tool_use` → flattened to a `text` block describing the call:
 *     "[Tool: <name>] <JSON of input>". claude.ai's tool ecosystem
 *     (web_search, code interpreter, drive, etc.) doesn't exist in
 *     Claude Code, so the calls can't be replayed — but visible
 *     context is better than silent loss.
 *   - `tool_result` → flattened to a `text` block: "[Tool result] <content>".
 *
 * The output round-trips through this repo's own `readJsonl` reader,
 * which is the strongest structural guarantee we have without testing
 * against Claude Code's loader directly.
 */

export interface ClaudeCodeJsonlOptions {
  /** Workspace path the imported chat will be tied to. */
  readonly cwd: string;
  /** Active git branch in the workspace, or empty string if none. */
  readonly gitBranch: string;
  /** Claude Code extension version for the `version` field. */
  readonly version: string;
  /**
   * Optional pre-supplied sessionId. Defaults to a fresh UUID v4.
   * Useful for tests that need deterministic output.
   */
  readonly sessionId?: string;
  /**
   * Override UUID generator. Tests pass a deterministic counter; the
   * runtime path uses crypto.randomUUID via the default.
   */
  readonly uuid?: () => string;
}

export interface ClaudeCodeJsonlResult {
  /** Newline-delimited JSON, one event per message. */
  readonly jsonl: string;
  /** The sessionId that the events use (also the suggested filename stem). */
  readonly sessionId: string;
}

const ENTRYPOINT = 'claude-vscode';
const USER_TYPE = 'external';
const PERMISSION_MODE = 'acceptEdits';

export function formatAsClaudeCodeJsonl(
  conversation: ClaudeAiConversation,
  opts: ClaudeCodeJsonlOptions,
): ClaudeCodeJsonlResult {
  const newUuid = opts.uuid ?? randomUUID;
  const sessionId = opts.sessionId ?? newUuid();

  const lines: string[] = [];
  let parentUuid: string | null = null;

  for (const message of conversation.chat_messages) {
    const eventUuid = newUuid();
    const text = collapseToText(message.content, message.text);

    if (message.sender === 'human') {
      const event = {
        parentUuid,
        isSidechain: false,
        promptId: newUuid(),
        type: 'user',
        message: {
          role: 'user',
          content: [{ type: 'text', text }],
        },
        uuid: eventUuid,
        timestamp: message.created_at,
        permissionMode: PERMISSION_MODE,
        userType: USER_TYPE,
        entrypoint: ENTRYPOINT,
        cwd: opts.cwd,
        sessionId,
        version: opts.version,
        gitBranch: opts.gitBranch,
      };
      lines.push(JSON.stringify(event));
    } else {
      // assistant
      const event = {
        parentUuid,
        isSidechain: false,
        message: {
          model: 'claude-imported-from-claude-ai',
          id: `msg_imported_${shortRandom(newUuid)}`,
          type: 'message',
          role: 'assistant',
          content: [{ type: 'text', text }],
          stop_reason: 'end_turn',
        },
        requestId: `exportal-imported-${sessionId}`,
        type: 'assistant',
        uuid: eventUuid,
        timestamp: message.created_at,
        userType: USER_TYPE,
        entrypoint: ENTRYPOINT,
        cwd: opts.cwd,
        sessionId,
        version: opts.version,
        gitBranch: opts.gitBranch,
      };
      lines.push(JSON.stringify(event));
    }
    parentUuid = eventUuid;
  }

  // NDJSON convention: trailing newline, every line including the last
  // ends with `\n` so `cat file.jsonl another.jsonl` round-trips.
  return { jsonl: lines.length === 0 ? '' : lines.join('\n') + '\n', sessionId };
}

/**
 * Walk the claude.ai content blocks and return a single text string,
 * applying the lossy block-to-text conversions described in the file
 * header. Falls back to `message.text` (the convenience field) if the
 * content array yields nothing useful.
 */
function collapseToText(content: ClaudeAiMessage['content'], fallback: string | undefined): string {
  const parts: string[] = [];
  for (const block of content) {
    const piece = blockToText(block);
    if (piece !== null) parts.push(piece);
  }
  if (parts.length > 0) return parts.join('\n\n');
  return typeof fallback === 'string' ? fallback : '';
}

function blockToText(block: ClaudeAiContentBlock): string | null {
  if (block.type === 'text') return block.text;
  if (block.type === 'tool_use') {
    // Best-effort serialization. JSON.stringify covers any shape the
    // API returns; if it fails (cyclic, BigInt) we still emit the
    // marker so the user sees a tool was called.
    let inputDescription = '';
    try {
      inputDescription = JSON.stringify(block.input ?? {});
    } catch {
      inputDescription = '<unserializable>';
    }
    return `[Tool: ${block.name}] ${inputDescription}`;
  }
  if (block.type === 'tool_result') {
    const content = block.content;
    let body: string;
    if (typeof content === 'string') {
      body = content;
    } else {
      try {
        body = JSON.stringify(content ?? '');
      } catch {
        body = '<unserializable>';
      }
    }
    return `[Tool result] ${body}`;
  }
  // thinking blocks are skipped — see file header for why.
  return null;
}

function shortRandom(newUuid: () => string): string {
  // Strip dashes from a UUID for a compact, URL-safe identifier
  // suffix. Matches the look of Anthropic's own message IDs (no
  // dashes) without claiming to be one.
  return newUuid().replace(/-/g, '').slice(0, 24);
}
